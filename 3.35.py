#Дан список A = [A1, A2,...,A21]. Получить список списков:
#1-й элемент:	A1	0	0	0	0	0
#2-й элемент:	A2	A3	0	0	0	0
#3-й элемент:	A4	A5	A6	0	0	0
#4-й элемент:	A7	A8	A9	A10	0	0
#5-й элемент:	A11	A12	A13	A14	A15	0
#6-й элемент:	A16	A17	A18	A19	A20	A21.

a = list(range(1, 22))
res = []
k = 0
for i in range(6):
    tmp = a[k:k + i + 1] + [0] * (5 - i)
    res.append(tmp)
    k += i + 1
print(res)